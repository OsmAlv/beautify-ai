export function getTranslation(lang: Language, section: keyof typeof translations, key: string): string {
  const sectionTranslations = translations[section] as any;
  if (!sectionTranslations || !sectionTranslations[key]) {
    return key;
  }
  return sectionTranslations[key][lang] || sectionTranslations[key]['ru'] || sectionTranslations[key]['en'] || key;
}

export function useTranslation(section: keyof typeof translations) {
  const lang: Language = 'ru'; // Default to Russian
  return (key: string) => getTranslation(lang, section, key);
}
